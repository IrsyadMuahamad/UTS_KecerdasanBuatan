# ELIF = else if statement

nama = input("Nama anda siapa? ")

if nama == "saya iqbal": # kondisi 1
	print("Hai, anak bola!") # aksi true 1
elif nama == "saya mario": # kondisi 2
	print("Hai si anak gamer!!") # aksi true 2
elif nama == "mario": # kondisi 3
	print("Hai si humoris!") # aksi true 3
else:
	print("kamu siapa, saya tidak kenal!!!") # aksi false

print("ini adalah akhir dari percakapan")